package corejava;

public class Oops_Inheritance_A {

	public static void add() {
		int a = 190;
		int b = 80;
		System.out.println("The Addition of two values are: " + (a + b));
	}

	public static void sub() {
		int a = 190;
		int b = 80;
		System.out.println("The Addition of two values are: " + (a - b));
	}

	public static void main(String[] args) {
		add();
		sub();
	}

}